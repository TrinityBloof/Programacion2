    private void cmbReporteItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmbReporteItemStateChanged
        Integer codigo = (Integer) cmbReporte.getSelectedItem();
        DefaultTableModel model = (DefaultTableModel) tbDatos.getModel();
        model.setRowCount(0);
        Reporte.listaReporte = new ArrayList<>();
        ArrayList<Reporte> listaReporte = cargas.cargarReporte(codigo);
        Object[] arreglo = new Object[5];
        for (int i = 0; i < listaReporte.size(); i++) {
            arreglo[0] = listaReporte.get(i).getNombreAgente();
            arreglo[1] = listaReporte.get(i).getNombreDelincuente();
            arreglo[2] = listaReporte.get(i).getAliasDelincuente();
            arreglo[3] = listaReporte.get(i).getNombreTipoDelincuente();
            arreglo[4] = listaReporte.get(i).getNombreOrganizacion();
            model.addRow(arreglo);
        }
    }//GEN-LAST:event_cmbReporteItemStateChanged


