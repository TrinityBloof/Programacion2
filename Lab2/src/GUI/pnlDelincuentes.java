    public void mostrarDatosJTable() {
        DefaultTableModel model = (DefaultTableModel) tbDatos.getModel();
        model.setRowCount(0);
        Delincuentes.listaDelincuentes = new ArrayList<>();
        ArrayList<Delincuentes> listaDelincuentes = cargas.cargarDelincuentes();
        Object[] arreglo = new Object[10];
        for (int i = 0; i < listaDelincuentes.size(); i++) {
            arreglo[0] = listaDelincuentes.get(i).getCodigo();
            arreglo[1] = listaDelincuentes.get(i).getNombre();
            arreglo[2] = listaDelincuentes.get(i).getFecha_nacimiento();
            arreglo[3] = listaDelincuentes.get(i).getPais_origen();
            arreglo[4] = listaDelincuentes.get(i).getRecompensa();
            arreglo[5] = listaDelincuentes.get(i).getAlias();
            arreglo[6] = listaDelincuentes.get(i).getTipo_delincuente();
            arreglo[7] = listaDelincuentes.get(i).getOrganizacion();
            arreglo[8] = listaDelincuentes.get(i).getFecha_primer_delito();
            arreglo[9] = listaDelincuentes.get(i).getCantidad_delitos();
            model.addRow(arreglo);
        }
    }
    
