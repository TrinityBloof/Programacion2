    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        int recompensa = 0, organizacion = 0, cantidadDelitos = 0;
        int codigo = Integer.parseInt(txtCodigo.getText());
        String nombre = txtNombre.getText();
        Date fechaNacimiento = jDateChooser1.getDate();
        String paisOrigen = txtPaisOrigen.getText();
        if (txtRecompensa.getText().equals("")) {
            recompensa = 0;
        } else {
            recompensa = Integer.parseInt(txtRecompensa.getText());
        }
        String alias = txtAlias.getText();
        int tipoDelincuente = Integer.parseInt(txtCodTipoDelincuente.getText());
        if (txtCodOrganizacion.getText().equals("")) {
            organizacion = 0;
        } else {
            organizacion = Integer.parseInt(txtCodOrganizacion.getText());
        }
        Date fechaPrimerDelito = jDateChooser2.getDate();
        if (txtCantidadDelitos.getText().equals("")) {
            cantidadDelitos = 0;
        } else {
            cantidadDelitos = Integer.parseInt(txtCantidadDelitos.getText());
        }
        consultas.ejecutarSQL("UPDATE delincuentes SET nombre = '" + nombre + "', fecha_nacimiento = '" + fechaNacimiento + "', pais_origen = '" + paisOrigen + "', recompensa = " + recompensa + ", alias = '" + alias + "', tipo_delincuente = " + tipoDelincuente + ", organizacion = " + organizacion + ", fecha_primer_delito = '" + fechaPrimerDelito + "', cantidad_delitos = " + cantidadDelitos + " where codigo = "+codigo+"");
        limpiarCampos();
        mostrarDatosJTable();
    }//GEN-LAST:event_btnModificarActionPerformed

