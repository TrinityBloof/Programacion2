    public void mostrarDatosJTable() {
        DefaultTableModel model = (DefaultTableModel) tbDatos.getModel();
        model.setRowCount(0);
        TiposDelincuentes.listaTipoDelincuentes = new ArrayList<>();
        ArrayList<TiposDelincuentes> listaTipoDelincuentes = cargas.cargarTiposDelincuentes();
        Object[] arreglo = new Object[2];
        for (int i = 0; i < listaTipoDelincuentes.size(); i++) {
            arreglo[0] = listaTipoDelincuentes.get(i).getCodigo();
            arreglo[1] = listaTipoDelincuentes.get(i).getNombre();
            model.addRow(arreglo);
        }
    }
    
    public void mostrarDatosCombo() {
        cmbTiposDelincuentes.setModel(cargas.cargarCodigos());
    }

