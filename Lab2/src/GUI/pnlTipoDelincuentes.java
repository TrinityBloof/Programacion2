    private void cmbTiposDelincuentesItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmbTiposDelincuentesItemStateChanged
        Integer codigo = (Integer) cmbTiposDelincuentes.getSelectedItem();
        TiposDelincuentes tipoDelincuente = cargas.cargarUnTipoDelincuente(codigo);
        txtCodigo.setText(tipoDelincuente.getCodigo().toString());
        txtNombre.setText(tipoDelincuente.getNombre());
    }//GEN-LAST:event_cmbTiposDelincuentesItemStateChanged

    private void btnInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertarActionPerformed
        int codigo = Integer.parseInt(txtCodigo.getText());
        String nombre = txtNombre.getText();
        consultas.ejecutarSQL("INSERT INTO \"tipos_delincuentes\"(\"codigo\", \"nombre\") VALUES ("+codigo+", '"+nombre+"');");
        limpiarCampos();
        mostrarDatosJTable();
        mostrarDatosCombo();
    }//GEN-LAST:event_btnInsertarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        int codigo = Integer.parseInt(txtCodigo.getText());
        String nombre = txtNombre.getText();
        consultas.ejecutarSQL("UPDATE tipos_delincuentes SET nombre = '" + nombre + "' where codigo = "+codigo+"");
        limpiarCampos();
        mostrarDatosJTable();
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        Integer codigo = (Integer) cmbTiposDelincuentes.getSelectedItem();
        consultas.ejecutarSQL("DELETE FROM tipos_delincuentes WHERE codigo = '" + codigo + "'");
        limpiarCampos();
        mostrarDatosJTable();
        mostrarDatosCombo();
    }//GEN-LAST:event_btnEliminarActionPerformed


